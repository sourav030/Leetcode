class Solution {
  public:
    int findMin(vector<int>& arr) {
        // complete the function here
        int min_element=INT_MAX;
        for(int i=0; i<arr.size(); i++){
            if(arr[i]<min_element){
                min_element=arr[i];
            }
        }
        return min_element;
    }
};

